package Basic;

public class CharAndASCII {
    public static void main(String[] args) {

        // ASCII value of all character
        char capitalChar = 'A';
        char smallChar = 'a';
        char num = '1';
        int x = capitalChar;
        int y = smallChar;
        int z = num;
        System.out.println(x);
        System.out.println(y);
        System.out.println(z);
        System.out.println(z+5);
    }
    
}
